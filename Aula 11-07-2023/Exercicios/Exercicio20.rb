# 20- Faça um programa que verifique se uma pessoa possui renda mensal maior que 5000
# reais ou possui um imóvel próprio utilizando a estrutura condicional if..else e os operadores
# lógicos or, and.

renda = 4342
pImovel = true

if renda > 5000 && pImovel = false
    puts 'renda boa e sem casa'
elsif renda < 5000 && pImovel = true
    puts 'renda ruim e com casa'
elsif renda < 5000 && pImovel = false
    puts 'renda ruim e sem imovel'
elsif renda > 5000 && not(pImovel=false)
    puts 'riquin'
end


