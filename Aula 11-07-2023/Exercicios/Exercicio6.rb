# Faça um programa que verifique se um número é múltiplo de 2, 3 ou 5 utilizando a
# estrutura condicional if..elsif..else.

numero = 6

if numero % 2 == 0 
    puts 'numero multiplo de 2'
end
if numero % 3 == 0 
    puts 'numero multiplo de 3'
end
if numero % 5 == 0 
    puts 'numero multiplo de 5'    
end